final class GroundItemTile {

	/**
	 * The height of this tile.
	 */
	int height;

	/**
	 * The height of the item on this tile.
	 */
	int itemHeight;

	/**
	 * The key of this tile.
	 */
	int key;

	/**
	 * The primary (i.e. foremost) renderable on this tile.
	 */
	Renderable primary;

	/**
	 * The secondary renderable on this tile.
	 */
	Renderable secondary;

	/**
	 * The tertiary renderable on this tile.
	 */
	Renderable tertiary;

	/**
	 * The x coordinate of this tile.
	 */
	int x;

	/**
	 * The y coordinate of this tile.
	 */
	int y;

}